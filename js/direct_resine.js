$("#pc").hover(function() {
    // over
    $("#pc img").css({
        "opacity": "1",
        "transform": "scale(1.2)",
        "border-radius": "8%",
        "cursor": "pointer",
        "transition": "0.5s",
    })
}, function() {
    // out
    $("#pc img").css({
        "position": "",
        "width": "",
        "height": "",
        "border-radius": "",
        "transition": "",
        "z-index": "",
        "transform": ""
    })
});

